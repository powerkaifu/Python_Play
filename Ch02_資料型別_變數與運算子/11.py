# 試著將 1024 轉成二進位、八進位、十六進位
print(bin(1024))  # 0b10000000000
print(oct(1024))  # 0o2000
print(hex(1024))  # 0x400

# 試著將 0o65416 轉成二進位、十進位、十六進位
print(bin(0o65416))  # 0b110101100110110
print(int(0o65416))  # 34350
print(hex(0o65416))  # 0x86d6

# 試著將 0b1001001 轉成八進位、十進位、十六進位
print(oct(0b1001001))  # 0o111
print(int(0b1001001))  # 73
print(hex(0b1001001))  # 0x49

# 轉出來的都是字串
# 二進位會 0b 開頭的字串
# 八進位會 0o 開頭的字串
# 十六進位會 0x 開頭的字串
