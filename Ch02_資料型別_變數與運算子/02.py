# 轉成 10 進位
# (a) 16進位:a0ff、121ab、acd123
print(int('a0ff', 16))
print(int('121ab', 16))
print(int('acd123', 16))

# (b) 8進位:7231、754、567
print(int('7231', 8))
print(int('754', 8))
print(int('567', 8))

# (c) 2進位: 11001010、1001001、110101000100010110
print(int('11001010', 2))
print(int('1001001', 2))
print(int('110101000100010110', 2))
